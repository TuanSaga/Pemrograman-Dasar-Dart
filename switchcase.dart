void main(){
  var nilai = 'A'; //A B C D  value dari variabel nilai 
  switch (nilai){
    case 'A':
      print('sangat baik');
      break;
    case 'B':
      print('baik');
      break;
    case 'C':
      print('cukup');
      break;
    case 'D':
      print('kurang');
      break;
    default:
      print('tidak valid');
      break;

  }


}